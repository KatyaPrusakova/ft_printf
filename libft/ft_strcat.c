#include "libft.h"

char		*ft_strcat(char *dst, const char *src)
{
	size_t		i;
	size_t		j;

	i = 0;
	j = 0;
	while (dst && dst[i] != '\0')
	{
		i++;
	}
	while (src && src[j] != '\0')
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (dst);
}
