#include "libft.h"

char		*ft_strdup(const char *s1)
{
	int		i;
	char	*new;

	i = 0;
	while (s1[i] != '\0')
		i++;
	new = (char *)malloc(sizeof(*new) * (i + 1));
	if (!new)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
